import { hiddenArchetypeId, type ArchetypeId, type BroadcastMessage, type HotspotDialogueTask, type PersonaFragment } from './types'
import type { GeneratedAssetQualityStatus, PersonalityAssetManifest } from './assetManifest'

export type WorldLayerShape = 'panel' | 'tower' | 'arch' | 'pool' | 'nest' | 'console' | 'booth' | 'beam' | 'platform'
export type WorldHotspotKind = 'object' | 'npc'

export interface WorldDecoration {
  id: string
  label: string
  x: number
  y: number
  width: number
  height: number
  shape: WorldLayerShape
  depth: 'rear' | 'mid' | 'front'
}

export interface WorldHotspot {
  id: string
  label: string
  kind: WorldHotspotKind
  icon: string
  x: number
  y: number
  accent: 'primary' | 'secondary' | 'accent'
  title: string
  summary: string
  actionLabel: string
  reaction: string
  lines: string[]
  dialogue?: HotspotDialogueTask
  fragmentId?: string
  agentPersona?: string
}

export interface WorldSpriteAsset {
  sourceType?: 'spritesheet' | 'frames' | 'image'
  imagePath?: string
  framesDir?: string
  frameCount?: number
  frameWidth: number
  frameHeight: number
  columns: number
  rows: number
  frameIndex: number
  scale: number
  layout?: 'player-8dir-8x8-v1' | 'single-image-v1' | 'legacy'
  qualityStatus?: GeneratedAssetQualityStatus
  animations?: {
    walkLeft?: { start: number; end: number; frameRate?: number }
    walkRight?: { start: number; end: number; frameRate?: number }
    walkDown?: { start: number; end: number; frameRate?: number }
    walkDownLeft?: { start: number; end: number; frameRate?: number }
    walkDownRight?: { start: number; end: number; frameRate?: number }
    walkUp?: { start: number; end: number; frameRate?: number }
    walkUpLeft?: { start: number; end: number; frameRate?: number }
    walkUpRight?: { start: number; end: number; frameRate?: number }
    idleDown?: number
    idleDownLeft?: number
    idleDownRight?: number
    idleFront?: number
    idleRight?: number
    idleUp?: number
    idleUpLeft?: number
    idleUpRight?: number
    idleBack?: number
    idleLeft?: number
  }
}

export interface WorldAssetBundle {
  backgroundImage?: string
  mapTmj?: string
  walkableGrid?: string
  playerSprite?: WorldSpriteAsset
  hotspotSprites?: Partial<Record<string, WorldSpriteAsset>>
}

export interface WorldConfig {
  id: ArchetypeId
  title: string
  subtitle: string
  atmosphere: string
  promptCue: string
  spawn: { x: number; y: number }
  landmark: {
    label: string
    x: number
    y: number
    width: number
    height: number
    shape: WorldLayerShape
  }
  decorations: WorldDecoration[]
  hotspots: WorldHotspot[]
  mission: string
  assets?: WorldAssetBundle
  generatedAssets?: PersonalityAssetManifest
  broadcasts?: BroadcastMessage[]
  fragments?: PersonaFragment[]
  escapeTitle?: string
  escapeSubtitle?: string
}

